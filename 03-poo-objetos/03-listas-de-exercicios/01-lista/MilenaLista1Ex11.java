package com.mycompany.milenalista1ex11;

/* Lista 1, exercício 11 (3.1):
   Escrever 20 vezes a mensagem “Eu vou aprender Programação Orientada a Objetos!”. */

public class MilenaLista1Ex11 {

    public static void main(String[] args) {        
        for (int i = 1; i <= 20; i++) {
            System.out.println(i + ". Eu vou aprender Programação Orientada a Objetos!");
        }
    }
}
