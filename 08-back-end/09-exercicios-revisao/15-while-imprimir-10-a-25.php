// Exercício 15: Crie um script que use um loop while para imprimir os números de 1 a 25.

$number = 1; // Primeiro número

// Loop while para imprimir números de 1 a 25
while ($number <= 25) {
    echo $number . "\n"; // Imprime o número atual
    $number++; // Aumenta o número em 1 para continuar o loop até o último número
}

?>