<?php

// Aula de 03/09/2024 - Lista de exercícios de PHP (For)
// Exercício 4: Imprima os números de um valor N até 1 em ordem decrescente.

// Define o valor inicial de N
$n = 10;

// Loop para imprimir os números de N até 1 em ordem decrescente
for ($i = $n; $i >= 1; $i--) {
    // Exibe o número atual seguido de um espaço
    echo $i . " ";
}

?>