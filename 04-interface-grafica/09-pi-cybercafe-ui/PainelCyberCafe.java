    private boolean checarBtComputadores() {
        for (int i = 0; i < 6; i++) {
            if (!selecionadosComputadores[i]) {
                todosSelecionados = false;
                break;
            }
            todosSelecionados = true;
        }
        
        if (todosSelecionados) return true;
        else return false;
    }
    
